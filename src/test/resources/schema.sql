DROP TABLE IF EXISTS islem;
DROP TABLE IF EXISTS kitap;
DROP TABLE IF EXISTS yazar;
DROP TABLE IF EXISTS tur;
DROP TABLE IF EXISTS ogrenci;

CREATE TABLE ogrenci (
    ogrno BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ad VARCHAR(45) NOT NULL,
    soyad VARCHAR(45) NOT NULL,
    cinsiyet VARCHAR(1) NOT NULL,
    sinif VARCHAR(3),
    puan INTEGER,
    dtarih VARCHAR(20)
);

CREATE TABLE islem (
    islemno BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ogrno BIGINT NOT NULL,
    kitapno BIGINT NOT NULL,
    atarih VARCHAR(20) NOT NULL,
    vtarih VARCHAR(20)
);

CREATE TABLE kitap (
    kitapno BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ad VARCHAR(45) NOT NULL,
    puan INTEGER NOT NULL,
    yazarno BIGINT NOT NULL,
    turno BIGINT NOT NULL
);

CREATE TABLE yazar (
    yazarno BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ad VARCHAR(45) NOT NULL,
    soyad VARCHAR(45) NOT NULL
);

CREATE TABLE tur (
    turno BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ad VARCHAR(45) NOT NULL
);
